    <script>
const images = document.querySelectorAll(".gallery-container img");
const lightbox = document.querySelector(".lightbox");
const lightboxImg = document.querySelector(".lightbox img");
const closeBtn = document.querySelector(".close");
const prevBtn = document.querySelector(".prev");
const nextBtn = document.querySelector(".next");

let currentIndex = 0;
let direction = 1; // 1 for next (right), -1 for previous (left)

function showImage(index, slideDirection) {
  lightboxImg.classList.remove("slide-in");

  // Add slide-out effect based on direction
  if (slideDirection === 1) {
    lightboxImg.classList.add("slide-left");
  } else {
    lightboxImg.classList.add("slide-right");
  }

  setTimeout(() => {
    lightboxImg.src = images[index].src;

    // Remove slide-out effect and add slide-in effect
    lightboxImg.classList.remove("slide-left", "slide-right");
    lightboxImg.classList.add("slide-in");
  }, 300); // This should match the CSS transition time
}

images.forEach((img, index) => {
  img.addEventListener("click", () => {
    currentIndex = index;
    lightbox.style.display = "flex";
    showImage(currentIndex, 1);
  });
});

nextBtn.addEventListener("click", () => {
  direction = 1;
  currentIndex = (currentIndex + 1) % images.length;
  showImage(currentIndex, direction);
});

prevBtn.addEventListener("click", () => {
  direction = -1;
  currentIndex = (currentIndex - 1 + images.length) % images.length;
  showImage(currentIndex, direction);
});

closeBtn.addEventListener("click", () => {
  lightbox.style.display = "none";
});

